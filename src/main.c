/**
 * @brief Ajusta a exposição do sensor.
 * @param SH_cyc Número de ciclos SH para o timer.
 */
void changeExposure(int32_t SH_cyc) {
  int32_t min_icg = 1440000;
  int32_t ICG_cyc;
  int32_t icgN = (min_icg / SH_cyc);

  if (icgN % 1) {  // Apenas 1 sensor
      icgN++;
  }
  ICG_cyc = icgN * SH_cyc;

  TIM2->ARR = ICG_cyc - 1;
  TIM2->CCR1 = 2 * SH_cyc - 1;
  TIM5->ARR = SH_cyc - 1;
  TIM5->CCR3 = SH_cyc / 2 - 1;

  LL_TIM_GenerateEvent_UPDATE(TIM2);
  LL_TIM_GenerateEvent_UPDATE(TIM5);

  // Alinha a saída
  TIM3->CNT = 0;
  TIM2->CNT = 36;
  TIM5->CNT = 0;
  TIM4->CNT = 0;
}

/**
 * @brief this function is a callback to the period elapsed of the timer
 * also, when the period is compete we do check if some message is received from USB, if so
 * we can process that message and start ADC conversions.
 *
 * @param TIM_HandleTypeDef *htim is the timer reference for the callback.
 * @retval NONE
 */
uint16_t exposure_wipe = 0;
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim){


	if(htim == &htim2){
		HAL_GPIO_WritePin(GPIOC, GPIO_PIN_13, GPIO_PIN_RESET);
		if(read_type){
			HAL_GPIO_WritePin(GPIOC, GPIO_PIN_13, GPIO_PIN_SET);
			SH_c = (uint32_t)(t_exp/PMCLK);

			if(SH_c-1 != (SH_cycles)){
				changeExposure(SH_c);
				exposure_wipe = 4;
			}

			typeStrat = read_type;
			avg_count = n_avg;
			memset(USB_Buffer, 0, sizeof(USB_Buffer));
			read_type = 0;
		}
		else if(exposure_wipe){
			exposure_wipe--;
		}
		else if(avg_count){
			HAL_ADC_Start_DMA(&hadc1, (uint32_t*) DMA_Buffer, CCDBufferSZ);
		}

	}
}


/**
 * @brief this function processes the data from the ADC read
 * and stores it in the USB_Buffer. It averages the data if needed.
 *
 * @param NONE
 * @retval NONE
 */
void processData() {
  for (int i = 0; i < SensorSZ; i++) {
      USB_Buffer[i] += DMA_Buffer[i];  // Acumula para média
  }
}


/**
 * @brief Callback de conversão completa do ADC.
 * Processa os dados e prepara para envio via USB.
 */
void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef* hadc) {
  processData(); 
  avg_count--;

  if (!avg_count) {
      for (int i = 0; i < SensorSZ; i++) {
          USB_Buffer[i] /= n_avg;  // Finaliza a média
      }
      
      // Prepara metadados
      USB_Buffer[SensorSZ] = 0;  // Reservado
      USB_Buffer[SensorSZ + 1] = (uint16_t)(SensorSZ & 0xFFFF);
      USB_Buffer[SensorSZ + 2] = (uint16_t)((SensorSZ >> 16) & 0xFFFF);
      USB_Buffer[SensorSZ + 3] = 69;  // 'E'
      USB_Buffer[SensorSZ + 4] = 78;  // 'N'
      USB_Buffer[SensorSZ + 5] = 68;  // 'D'

      // Transmite os dados via USB
      CDC_Transmit_FS((uint8_t*)USB_Buffer, (SensorSZ + 6) * sizeof(uint16_t));
  }
}

